# Summarize results

import pandas as pd

rows = []

for name, report in reports.items():
    scores = {score.key: score for score in report.scores}

    baseline = scores["baseline"].metrics
    anchor_only = scores["anchor_only"].metrics
    full = scores["full"].metrics

    rows.append(
        {
            "Dataset": name,
            "Weight": WEIGHTS[name],
            "Plain RAG R@K": baseline.recall_at_k,
            "Anchor Only R@K": anchor_only.recall_at_k,
            "AnRAG Full R@K": full.recall_at_k,
            "Delta Full-Baseline": round(
                full.recall_at_k - baseline.recall_at_k,
                4,
            ),
            "Zero Gold": scores["full"].gold_sanity.zero_gold_count,
            "Avg Gold Size": scores["full"].gold_sanity.avg_gold_size,
        }
    )

df = pd.DataFrame(rows)

df["Weighted Full"] = (
    df["Weight"]
    * df["AnRAG Full R@K"]
)

display(df)

print(
    "FINAL MIXED SCORE =",
    round(df["Weighted Full"].sum(), 4),
)

