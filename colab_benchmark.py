# Build SciFact subset in BeIR layout

from beir import util
from beir.datasets.data_loader import GenericDataLoader

beir_root = WORK / "beir"
beir_root.mkdir(parents=True, exist_ok=True)

scifact_path = Path(
    util.download_and_unzip(
        "https://public.ukp.informatik.tu-darmstadt.de/thakur/BEIR/datasets/scifact.zip",
        str(beir_root),
    )
)

scifact_corpus, scifact_queries, scifact_qrels = (
    GenericDataLoader(str(scifact_path)).load(split="test")
)

query_ids = [
    qid
    for qid in scifact_queries.keys()
    if scifact_qrels.get(qid)
][:N_SCIFACT]

needed_docs = set()
for qid in query_ids:
    needed_docs.update(scifact_qrels[qid].keys())

negative_candidates = [
    doc_id
    for doc_id in scifact_corpus.keys()
    if doc_id not in needed_docs
]

negative_count = max(
    0,
    min(
        MAX_SCIFACT_DOCS - len(needed_docs),
        len(negative_candidates),
    ),
)

selected_docs = (
    set(needed_docs)
    | set(random.sample(negative_candidates, negative_count))
)

mini_scifact = WORK / "scifact_beir_subset"
(mini_scifact / "qrels").mkdir(parents=True, exist_ok=True)

write_jsonl(
    mini_scifact / "corpus.jsonl",
    [
        {
            "_id": doc_id,
            "title": scifact_corpus[doc_id].get("title", ""),
            "text": scifact_corpus[doc_id].get("text", ""),
        }
        for doc_id in sorted(selected_docs)
    ],
)

write_jsonl(
    mini_scifact / "queries.jsonl",
    [
        {
            "_id": qid,
            "text": scifact_queries[qid],
        }
        for qid in query_ids
    ],
)

with (mini_scifact / "qrels" / "test.tsv").open(
    "w",
    encoding="utf-8",
) as handle:
    for qid in query_ids:
        for doc_id, score in scifact_qrels[qid].items():
            if doc_id in selected_docs:
                handle.write(
                    f"{qid}\t{doc_id}\t{int(score)}\n"
                )

print("SciFact queries:", len(query_ids))
